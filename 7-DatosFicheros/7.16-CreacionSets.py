# cración de un set por extensión
set1 = {1, 2, 3, 4, 5}
print (set1)

#transforma una lista o tupla en set
lista = [1, 2, 3, 4, 5]
tupla = ("a", "b", "c")
set2 = set(lista)
set3 = set (tupla)
print (set2)
print (set3)

#cración por adición de elementos
set4 = set() #set vacío
set4.add("uno")
set4.add(2)
set4.add(3.1416)
print (set4)
