nombres = ("Lara", "María", "Selene", "Luis")

# Bucle for
for n in nombres:
    print(f"¡Buenos días, {n}!")
    
# Bucle for indexado
for i in range(len(nombres)):
    print(f"¡Buenos días, {nombres[i]}!")

# Bucle while
i = 0
while i < len(nombres):
    print(f"¡Buenos días, {nombres[i]}!")
    i += 1
