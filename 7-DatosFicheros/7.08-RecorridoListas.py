listaPrimos = [1,3,5,7,11,13,17]

#recorrer lista con for
for elto in listaPrimos:
  print(elto)

#recorrer lista con for indexado
for i in range (len(listaPrimos)):
  print(listaPrimos[i])

#recorrer lista con while
i = 0
while i < len(listaPrimos):
  print(listaPrimos[i])
  i = i + 1