import math

# Calcular la raíz cuadrada de un número
num = 25
sqrt_num = math.sqrt(num)
print("La raíz cuadrada de", num, "es", sqrt_num)

# Calcular el coseno de un ángulo en radianes
angulo = 45
cos_angulo = math.cos(math.radians(angulo))
print("El coseno de", angulo, "grados es", cos_angulo)

# Calcular el seno de un ángulo en radianes
angulo = 60
sin_angulo = math.sin(math.radians(angulo))
print("El seno de", angulo, "grados es", sin_angulo)

# Calcular el logaritmo natural de un número
num = 10
ln_num = math.log(num)
print("El logaritmo natural de", num, "es", ln_num)

# Calcular el máximo común divisor de dos números
num1 = 24
num2 = 36
mcd= math.gcd(num1, num2)
print("El máximo común divisor de", num1, "y", num2, "es", mcd)


