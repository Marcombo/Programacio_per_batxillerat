# Ejemplo de operador de asignación simple
x = 5
print(x)  # Salida: 5

# Ejemplo de operador de asignación de suma
x = 5
x += 3
print(x)  # Salida: 8

# Ejemplo de operador de asignación de resta
x = 5
x -= 3
print(x)  # Salida: 2

# Ejemplo de operador de asignación de multiplicación
x = 5
x *= 3
print(x)  # Salida: 15

# Ejemplo de operador de asignación de división
x = 5
x /= 3
print(x)  # Salida: 1.6666666666666667

# Ejemplo de operador de asignación de módulo
x = 5
x %= 3
print(x)  # Salida: 2

# Ejemplo de operador de asignación de división entera
x = 5
x //= 3
print(x)  # Salida: 1

# Ejemplo de operador de asignación de exponenciación
x = 5
x **= 3
print(x)  # Salida: 125
